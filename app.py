import streamlit as st

# כותרת הדשבורד
st.title("מערכת ניהול פרויקטים - הבית הירוק")

# ניווט בתפריטים
menu = ["עמוד ראשי", "תפריט לקוחות", "תפריט פרויקטים", "תפריט תשלומים", "תפריט מנהל מערכת"]
choice = st.sidebar.selectbox("בחר תפריט", menu)

# עמוד ראשי
if choice == "עמוד ראשי":
    st.header("עמוד ראשי")
    st.write("תקציר סטטוס פרויקט:")
    st.write("• ציר זמן אינטראקטיבי של פרויקטים.")
    st.write("• התראות עדכניות (מסמכים חסרים, תשלומים פתוחים, ביקורים קרובים).")
    st.write("• כפתורים לגישה מהירה לפעולות נפוצות.")

# תפריט לקוחות
elif choice == "תפריט לקוחות":
    st.header("תפריט לקוחות")
    st.write("• פתיחת פניה חדשה.")
    st.write("• עדכון פרטים אישיים.")
    st.write("• ניהול מסמכים נדרשים.")
    st.write("• מעקב אחר סטטוס הפרויקט.")

# תפריט פרויקטים
elif choice == "תפריט פרויקטים":
    st.header("תפריט פרויקטים")
    st.write("• רשימת פרויקטים פעילים/סגורים.")
    st.write("• ציר זמן לפרויקטים פעילים.")
    st.write("• עדכון סטטוס פרויקט.")
    st.write("• סיכום מלאי ושימוש.")

# תפריט תשלומים
elif choice == "תפריט תשלומים":
    st.header("תפריט תשלומים")
    st.write("• צפייה בחשבוניות וקבלות.")
    st.write("• תשלום אגרות אונליין.")
    st.write("• פריסת תשלומים לפי פרויקט.")

# תפריט מנהל מערכת
elif choice == "תפריט מנהל מערכת":
    st.header("תפריט מנהל מערכת")
    st.write("• ניהול תאריכים ומועדים.")
    st.write("• דוחות ביצועים.")
    st.write("• תיעוד וארכיון פרויקטים.")
    st.write("• ניהול מלאי והזמנות.")
